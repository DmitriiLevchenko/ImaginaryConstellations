package com.example.imaginaryconstellations.util;

public class Pointer {
    private float X;
    private float Y;

    public Pointer(float x, float y) {
        X = x;
        Y = y;
    }

    public float getX() {
        return X;
    }

    public void setX(float x) {
        X = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }
}
